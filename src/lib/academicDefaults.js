// Класифікація пункту з appendicesAiGen за типом вмісту — визначає який
// спеціалізований білдер промпту використати в doGenAppendices.
// Порядок перевірки важливий: специфічніші ключові слова йдуть раніше.
const APPENDIX_TYPE_RULES = [
  { type: "program",    kw: /програм|технологі|методик(?!и\s+\(порожн)|стратегі|кампані|медіаплан|проєкт\s+тур|тур-продукт/i },
  { type: "scheme",     kw: /схем|структур|swot|uml|хронологі|порівняння концепц|порівняння позиц/i },
  { type: "data_table", kw: /таблиц|масив|дан(?:і|их)|розрахунк|прогноз|модел|графік/i },
  { type: "document",   kw: /перелік|список|глосарій|зразки.*документ|опис артефакт/i },
];

export function classifyAppendixItem(text) {
  const t = (text || "").toLowerCase();
  for (const { type, kw } of APPENDIX_TYPE_RULES) {
    if (kw.test(t)) return type;
  }
  return "form"; // дефолт: бланки, протоколи, анкети, гайди інтерв'ю, конспекти, кейси
}

// ── Public API ──

export function detectSpecialty(text) {
  const t = (text || "").toLowerCase();
  for (const { cat, kw } of SPECIALTY_MAP) {
    if (kw.test(t)) return cat;
  }
  return null;
}

export function normalizeWorkType(type, course) {
  if (/магістерськ|master/i.test(type || "")) return "master";
  if (/бакалаврськ|диплом(?!ний\s+проєкт\s+з)|диплом.*бакал/i.test(type || "")) return "bachelor";
  if (/курсов/i.test(type || "")) {
    const c = parseInt(course, 10);
    return (!c || c <= 2) ? "course_1_2" : "course_3_4";
  }
  return null;
}

/**
 * Returns research defaults for the given specialty + work type + topic.
 * Returns null if no match found (caller should skip injecting hints).
 */
export function getAcademicDefaults(specialty, workType, course, topic) {
  const cat = detectSpecialty((specialty || ""));
  if (!cat) return null;

  const wt = normalizeWorkType(workType, course);
  if (!wt) return null;

  const entry = TABLE[cat]?.[wt];
  if (!entry) return null;

  // Resolve "або" between main and alternative research type
  let researchType = entry.researchType;
  if (entry.researchTypeAlt) {
    const t = topic || "";
    const useAlt = entry.altOnDocKw && DOCUMENT_KW.test(t) && !EMPIRICAL_KW.test(t);
    if (useAlt) researchType = entry.researchTypeAlt;
  }

  return {
    researchType,
    methods: entry.methods || [],
    instrumentsCount: entry.instrumentsCount || null,
    appendicesAiGen: entry.appendicesAiGen || [],
    appendicesUserProvide: entry.appendicesUserProvide || [],
    notes: entry.notes || "",
  };
}
